package me.theentropyshard.crlauncher.gui;

import me.theentropyshard.crlauncher.cosmic.Version;

import javax.swing.*;
import java.awt.*;

public class VersionListCellRenderer extends DefaultListCellRenderer {
    public VersionListCellRenderer() {

    }

    @Override
    public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
        super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);

        if (value instanceof Version v) {
            this.setText(v.getId() + " (" + v.getReleaseTime() + ")");
        }

        return this;
    }
}
